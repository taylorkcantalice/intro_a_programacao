#include <stdio.h>

/****
* A função implementada nesse programa possui um erro, que inviabilizava o
* cálculo correto da média, devido a escolha do tipo de valor que a função
* produz ser do tipo primitivo inteiro (int), como também na escolha do
* tipo interiro que a função recebe como valores do parâmetro. Como pode
* ser visto a seguir:
*
* int Media(int valor1, int valor2){
*   int soma = valor1 + valor2;
*   return soma / 2;
* }
*
* Então, uma possível correção seria substituir o tipo de valor e o valor
* do parâmetro pelo float', que armazena números com ponto flutuante
* (reais) com precisão simples, sendo o ideal para o cálculo de médias.
****/

/****
* Media(): Calcula a media de dois valores.
*
* Parametros:
*    valor1 (entrada): o primeiro valor.
*    valor2 (entrada): o segundo valor.
*
* Retorno: retorna sempre a media dos valores recebidos.
****/
float Media(float valor1, float valor2){
    float soma = valor1 + valor2;
    return soma / 2;
}

int main(void){
    float nota1, nota2; /* os valores a serem lidos do usuario */
    float media;        /* para calcular e guardar o resultado */

    /* leitura dos valores */
    printf("Digite a primeira nota: ");
    scanf("%f", &nota1);
    printf("Digite a segunda nota: ");
    scanf("%f", &nota2);

    /* calular a media a partir da chamada da funcao */
    media = Media(nota1, nota2);

    /* imprime o resultado */
    printf("A media das notas %.1f e %.1f eh %.1f\n", nota1, nota2, media);

    return 0;
}
